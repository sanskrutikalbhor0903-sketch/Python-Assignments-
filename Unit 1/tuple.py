#Create a Tuple
t1 = (10, 20, 30)
#Acess elements 
print("First Element:", t1[0])
print("Last Element:", t1[-1])
#Nested Tuple
t2 = (1, 2, (3, 4))
print("Nested Tuple:", t2)
#Repetition of Tuple
t3 = (t1*2)
print("Repeated Tuple:", t3)
#Concatenated of Tuple
t4 = t1+(40, 50)
print("Concatenated Tuple:", t4)